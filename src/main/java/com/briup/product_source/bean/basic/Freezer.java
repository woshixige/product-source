package com.briup.product_source.bean.basic;

public class Freezer {
    private Integer fzId;

    private String fzName;

    private Integer fzNum;

    private String fzArea;

    private Integer fzQuantity;

    private String fzTemperature;

    private String fzHumidity;

    public Integer getFzId() {
        return fzId;
    }

    public void setFzId(Integer fzId) {
        this.fzId = fzId;
    }

    public String getFzName() {
        return fzName;
    }

    public void setFzName(String fzName) {
        this.fzName = fzName;
    }

    public Integer getFzNum() {
        return fzNum;
    }

    public void setFzNum(Integer fzNum) {
        this.fzNum = fzNum;
    }

    public String getFzArea() {
        return fzArea;
    }

    public void setFzArea(String fzArea) {
        this.fzArea = fzArea;
    }

    public Integer getFzQuantity() {
        return fzQuantity;
    }

    public void setFzQuantity(Integer fzQuantity) {
        this.fzQuantity = fzQuantity;
    }

    public String getFzTemperature() {
        return fzTemperature;
    }

    public void setFzTemperature(String fzTemperature) {
        this.fzTemperature = fzTemperature;
    }

    public String getFzHumidity() {
        return fzHumidity;
    }

    public void setFzHumidity(String fzHumidity) {
        this.fzHumidity = fzHumidity;
    }
}